/**
 * Escapes HTML entities to prevent XSS when rendering AI-generated text.
 * Call this BEFORE applying markdown-to-HTML transformations.
 */
export function escapeHtml(text: string): string {
  return text
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;')
}
